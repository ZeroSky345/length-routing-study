def overhead_class(strategy: SelectionStrategy) -> str:
    """Return the overhead complexity class of a strategy."""
    if isinstance(strategy, (GlobalSinkWindow, SqrtWindow, DecayedWindowSink)):
        return "O(1)"
    if isinstance(strategy, (BlockKNormTopK, CoverageTarget,
                              HierarchicalKNorm, AdaptiveCoverage)):
        return "O(L·D)"
    if isinstance(strategy, SampledAttentionTopK):
        return f"O({strategy.sample_rows}·L·D)"
    return "O(?)"
